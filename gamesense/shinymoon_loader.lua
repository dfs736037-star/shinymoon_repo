function SL.check_expected_common(item, now)
    local signed = item.signed
    SL.expect(signed.bundle_id == SL.config.bundle_id, signed.role .. " bundle_id mismatch")
    SL.expect(signed.channel == SL.config.channel, signed.role .. " channel mismatch")
    SL.expect(signed.min_loader_version <= SL.version, signed.role .. " requires a newer loader")
    SL.expect(signed.issued_at <= now + SL.limits.future_skew, signed.role .. " is issued too far in the future")
    SL.expect(signed.expires_at > now, signed.role .. " is expired")
end

function SL.check_high_water(role, item)
    local high_water = SL.state.high_water
    if type(high_water) ~= "table" or type(high_water.roles) ~= "table" then
        return
    end
    local previous = high_water.roles[role]
    if previous == nil then
        return
    end
    SL.exact_keys(previous, { "sha256", "version" }, "high_water." .. role)
    SL.assert_integer(previous.version, "high_water." .. role .. ".version", 1)
    SL.valid_digest(previous.sha256, "high_water." .. role .. ".sha256")
    SL.expect(item.signed.version >= previous.version, role .. " rollback below promoted version")
    SL.expect(item.signed.version ~= previous.version or item.digest == previous.sha256,
        role .. " equivocation: same version has a different digest")
end

function SL.verify_current_role(raw, role, now)
    local item = SL.verify_envelope(raw, role)
    SL.check_expected_common(item, now)
    SL.check_high_water(role, item)
    return item
end

function SL.assert_raw_reference(raw, reference, label)
    SL.expect(type(raw) == "string" and #raw == reference.length, label .. " raw length mismatch")
    local digest, digest_error = SL.sha256_hex(raw)
    SL.expect(digest ~= nil, digest_error)
    SL.expect(digest == reference.sha256, label .. " raw SHA-256 mismatch")
end

function SL.make_candidate_high_water(candidate)
    return {
        security_epoch = candidate.targets.signed.security_epoch,
        rollback_floor = candidate.targets.signed.rollback_floor,
        roles = {
            timestamp = { version = candidate.timestamp.signed.version, sha256 = candidate.timestamp.digest },
            snapshot = { version = candidate.snapshot.signed.version, sha256 = candidate.snapshot.digest },
            targets = { version = candidate.targets.signed.version, sha256 = candidate.targets.digest },
        },
    }
end

function SL.check_chain_epochs_and_floor(candidate)
    local epoch = candidate.timestamp.signed.security_epoch
    SL.expect(candidate.snapshot.signed.security_epoch == epoch and candidate.targets.signed.security_epoch == epoch,
        "metadata security_epoch mismatch")
    local high_water = SL.state.high_water
    if type(high_water) ~= "table" then
        return
    end
    local previous_epoch = SL.assert_integer(high_water.security_epoch or 0, "high_water.security_epoch", 0)
    local previous_floor = SL.assert_integer(high_water.rollback_floor or 0, "high_water.rollback_floor", 0)
    SL.expect(epoch >= previous_epoch, "security_epoch rollback")
    SL.expect(candidate.targets.signed.rollback_floor >= previous_floor, "rollback_floor rollback")
    SL.expect(candidate.targets.signed.version >= previous_floor, "targets below promoted rollback floor")
end

function SL.database_available()
    return type(database) == "table" and type(database.read) == "function"
        and type(database.write) == "function"
end

function SL.db_key(suffix)
    return "shinymoon_loader/" .. SL.config.bundle_id .. "/" .. SL.config.channel .. "/" .. suffix
end

function SL.load_persisted_high_water()
    if not SL.database_available() then
        return
    end
    local ok, row = pcall(database.read, SL.db_key("state"))
    if not ok or type(row) ~= "table" or type(row.high_water) ~= "table" then
        return
    end
    local valid = pcall(function()
        local high_water = row.high_water
        SL.assert_integer(high_water.security_epoch or 0, "high_water.security_epoch", 0)
        SL.assert_integer(high_water.rollback_floor or 0, "high_water.rollback_floor", 0)
        SL.expect(type(high_water.roles) == "table", "high_water.roles must be a table")
        for _, role in ipairs({ "timestamp", "snapshot", "targets" }) do
            local entry = high_water.roles[role]
            if entry ~= nil then
                SL.exact_keys(entry, { "sha256", "version" }, "high_water." .. role)
                SL.assert_integer(entry.version, "high_water." .. role .. ".version", 1)
                SL.valid_digest(entry.sha256, "high_water." .. role .. ".sha256")
            end
        end
    end)
    if valid then
        SL.state.high_water = row.high_water
    else
        SL.log("error", "stored high-water state is malformed and was ignored")
    end
end

function SL.verify_offline_bundle(slot, now)
    SL.expect(type(slot) == "table", "cache slot must be a table")
    local timestamp = SL.verify_current_role(slot.timestamp_raw, "timestamp", now)
    SL.assert_raw_reference(slot.snapshot_raw, timestamp.signed.snapshot, "snapshot")
    local snapshot = SL.verify_current_role(slot.snapshot_raw, "snapshot", now)
    SL.expect(snapshot.signed.version == timestamp.signed.snapshot.version, "timestamp snapshot version mismatch")
    SL.assert_raw_reference(slot.targets_raw, snapshot.signed.targets, "targets")
    local targets = SL.verify_current_role(slot.targets_raw, "targets", now)
    SL.expect(targets.signed.version == snapshot.signed.targets.version, "snapshot targets version mismatch")
    local candidate = {
        timestamp = timestamp, snapshot = snapshot, targets = targets,
        timestamp_raw = slot.timestamp_raw, snapshot_raw = slot.snapshot_raw, targets_raw = slot.targets_raw,
        graph = targets.graph, module_bytes = {}, source = "cache",
    }
    SL.check_chain_epochs_and_floor(candidate)
    SL.expect(type(slot.modules) == "table", "cached modules must be a table")
    local total = 0
    for index = 1, #candidate.graph.module_ids do
        local module = candidate.graph.modules[candidate.graph.module_ids[index]]
        local raw = slot.modules[module.id]
        SL.expect(type(raw) == "string" and #raw == module.length, module.id .. " cached byte length mismatch")
        local digest, digest_error = SL.sha256_hex(raw)
        SL.expect(digest ~= nil, digest_error)
        SL.expect(digest == module.sha256, module.id .. " cached SHA-256 mismatch")
        total = total + module.length
        candidate.module_bytes[module.id] = raw
    end
    SL.expect(total <= SL.limits.bundle_bytes, "cached bundle size exceeds the limit")
    candidate.total_bytes = total
    candidate.high_water = SL.make_candidate_high_water(candidate)
    return candidate
end

function SL.promote_candidate(candidate)
    if not SL.database_available() then
        return nil, "database is unavailable; skipping cache promotion"
    end
    local row = nil
    local ok_read, read_row = pcall(database.read, SL.db_key("state"))
    if ok_read and type(read_row) == "table" then
        row = read_row
    end
    local inactive = (row and row.active_slot == "a") and "b" or "a"
    local slot = {
        timestamp_raw = candidate.timestamp_raw,
        snapshot_raw = candidate.snapshot_raw,
        targets_raw = candidate.targets_raw,
        modules = candidate.module_bytes,
    }
    local ok, promote_error = pcall(function()
        database.write(SL.db_key("slot_" .. inactive), slot)
        local reread = database.read(SL.db_key("slot_" .. inactive))
        SL.verify_offline_bundle(reread, SL.now())
        database.write(SL.db_key("state"), { active_slot = inactive, high_water = candidate.high_water })
    end)
    if not ok then
        return nil, "cache promotion failed: " .. SL.safe_text(promote_error)
    end
    SL.state.high_water = candidate.high_water
    return true
end

function SL.copy_table(source)
    local out = {}
    for key, value in pairs(source) do
        out[key] = value
    end
    return out
end

function SL.readonly(target, label)
    return setmetatable({}, {
        __index = target,
        __newindex = function()
            error(label .. " is read-only", 2)
