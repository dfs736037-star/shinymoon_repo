        __metatable = false,
    })
end

function SL.make_module_env()
    local env = {
        assert = assert, error = error, ipairs = ipairs, next = next, pairs = pairs,
        pcall = pcall, select = select, tonumber = tonumber, tostring = tostring,
        type = type, unpack = unpack, xpcall = xpcall,
        setmetatable = setmetatable, getmetatable = getmetatable,
        rawget = rawget, rawset = rawset, rawequal = rawequal,
        string = SL.copy_table(string), table = SL.copy_table(table), math = SL.copy_table(math),
