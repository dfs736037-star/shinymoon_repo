function SL.hex_decode(value, expected_bytes)
    if type(value) ~= "string" or value:find("[^0-9a-f]") or (#value % 2) ~= 0 then
        return nil, "hex must be lowercase and even-length"
    end
    if expected_bytes and #value ~= expected_bytes * 2 then
        return nil, "hex has the wrong byte length"
    end
    local out = {}
    for index = 1, #value, 2 do
        local byte = tonumber(value:sub(index, index + 1), 16)
        if not byte then
            return nil, "hex contains an invalid byte"
        end
        out[#out + 1] = string.char(byte)
    end
    return table.concat(out)
end

function SL.hex_encode(value)
    local out = {}
    for index = 1, #value do
        out[index] = string.format("%02x", value:byte(index))
    end
    return table.concat(out)
end

function SL.base64_encode(value)
    local out = {}
    local index = 1
    while index <= #value do
        local a = value:byte(index) or 0
        local b = value:byte(index + 1)
        local c = value:byte(index + 2)
        local packed = a * 65536 + (b or 0) * 256 + (c or 0)
        local first = math.floor(packed / 262144) % 64
        local second = math.floor(packed / 4096) % 64
        local third = math.floor(packed / 64) % 64
        local fourth = packed % 64
        out[#out + 1] = SL.base64_alphabet:sub(first + 1, first + 1)
        out[#out + 1] = SL.base64_alphabet:sub(second + 1, second + 1)
        out[#out + 1] = b and SL.base64_alphabet:sub(third + 1, third + 1) or "="
        out[#out + 1] = c and SL.base64_alphabet:sub(fourth + 1, fourth + 1) or "="
        index = index + 3
    end
    return table.concat(out)
end

function SL.base64_decode(value, expected_bytes)
    if type(value) ~= "string" or #value == 0 or (#value % 4) ~= 0 then
        return nil, "base64 length is invalid"
    end
    local body = value:gsub("=+$", "")
    local padding = #value - #body
    if padding > 2 or body:find("=") or body:find("[^A-Za-z0-9+/]") then
        return nil, "base64 alphabet or padding is invalid"
    end
    local out = {}
    for index = 1, #value, 4 do
        local ca = value:sub(index, index)
        local cb = value:sub(index + 1, index + 1)
        local cc = value:sub(index + 2, index + 2)
        local cd = value:sub(index + 3, index + 3)
        local a = SL.base64_values[ca]
        local b = SL.base64_values[cb]
        local c = cc == "=" and 0 or SL.base64_values[cc]
        local d = cd == "=" and 0 or SL.base64_values[cd]
        if a == nil or b == nil or c == nil or d == nil then
            return nil, "base64 contains an invalid quartet"
        end
        if (cc == "=" and cd ~= "=") or (index < #value - 3 and (cc == "=" or cd == "=")) then
            return nil, "base64 padding is not final"
        end
        local packed = a * 262144 + b * 4096 + c * 64 + d
        out[#out + 1] = string.char(math.floor(packed / 65536) % 256)
        if cc ~= "=" then
            out[#out + 1] = string.char(math.floor(packed / 256) % 256)
        end
        if cd ~= "=" then
            out[#out + 1] = string.char(packed % 256)
        end
    end
    local decoded = table.concat(out)
    if expected_bytes and #decoded ~= expected_bytes then
        return nil, "base64 has the wrong decoded length"
    end
    if SL.base64_encode(decoded) ~= value then
        return nil, "base64 is not canonical padded encoding"
    end
    return decoded
end

function SL.wide(value)
    local cached = SL.crypto.wide_cache[value]
    if cached then
        return cached
    end
    local ffi = SL.crypto.ffi
    local wide = ffi.new("uint16_t[?]", #value + 1)
    for index = 1, #value do
        wide[index - 1] = value:byte(index)
    end
    wide[#value] = 0
    SL.crypto.wide_cache[value] = wide
    return wide
end

function SL.crypto_close()
    if not SL.crypto.open then
        return
