function SL.make_client_capability(resources)
    local wrapped = SL.copy_table(client)
    wrapped.set_event_callback = function(event_name, callback)
        SL.assert_ascii(event_name, "event name", 1, 64)
        SL.expect(type(callback) == "function", "event callback must be a function")
        local forwarder = function(...)
            if resources.revoked then
                return
            end
            return callback(...)
        end
        local ok, register_error = pcall(client.set_event_callback, event_name, forwarder)
        SL.expect(ok, "set_event_callback failed: " .. SL.safe_text(register_error))
        resources.events[#resources.events + 1] = { event = event_name, callback = callback, forwarder = forwarder }
    end
    wrapped.unset_event_callback = function(event_name, callback)
        for index = #resources.events, 1, -1 do
            local entry = resources.events[index]
            if entry.event == event_name and entry.callback == callback then
                pcall(client.unset_event_callback, event_name, entry.forwarder)
                table.remove(resources.events, index)
                return
            end
        end
    end
    wrapped.delay_call = function(seconds, callback, ...)
        SL.expect(type(callback) == "function", "delay_call callback must be a function")
        local count = select("#", ...)
        local arguments = { ... }
        local ok = pcall(client.delay_call, seconds, function()
            if resources.revoked then
                return
            end
            callback(unpack(arguments, 1, count))
        end)
        SL.expect(ok, "delay_call scheduling failed")
    end
    return SL.readonly(wrapped, "client capability")
end

function SL.make_capability(name, resources)
    if name == "client" then
        return SL.make_client_capability(resources)
    end
    local source = _G[name]
    SL.expect(type(source) == "table", "capability " .. name .. " is unavailable in this API")
    return SL.readonly(SL.copy_table(source), name .. " capability")
end

function SL.has_capability(module, name)
    for index = 1, #module.capabilities do
        if module.capabilities[index] == name then
            return true
        end
    end
    return false
end

function SL.compile_candidate(candidate)
    SL.expect(type(loadstring) == "function" and type(setfenv) == "function",
        "loadstring/setfenv are unavailable")
    local chunks = {}
    for index = 1, #candidate.graph.order do
        local module_id = candidate.graph.order[index]
        local module = candidate.graph.modules[module_id]
        local raw = candidate.module_bytes[module_id]
        SL.expect(type(raw) == "string" and #raw > 0, module_id .. " source is missing")
        SL.expect(not raw:find("\0", 1, true), module_id .. " contains a NUL byte")
        SL.expect(raw:byte(1) ~= 27, module_id .. " is precompiled bytecode")
        local chunk, compile_error = loadstring(raw, "@shinymoon_module_" .. module_id)
        SL.expect(chunk ~= nil, module_id .. " failed to compile: " .. SL.safe_text(compile_error))
        if not SL.has_capability(module, "full_env") then
            setfenv(chunk, SL.make_module_env())
        end
        chunks[module_id] = chunk
    end
    return chunks
end

function SL.cleanup_bundle(active)
    if type(active) ~= "table" then
        return
    end
    for index = #active.inited, 1, -1 do
        local entry = active.inited[index]
        if type(entry.descriptor.shutdown) == "function" then
            pcall(entry.descriptor.shutdown, entry.ctx)
        end
    end
    active.inited = {}
    active.resources.revoked = true
    if type(client) == "table" and type(client.unset_event_callback) == "function" then
        for index = #active.resources.events, 1, -1 do
            local entry = active.resources.events[index]
            pcall(client.unset_event_callback, entry.event, entry.forwarder)
        end
    end
    active.resources.events = {}
end

function SL.initialize_candidate(candidate, chunks)
    local resources = { revoked = false, events = {} }
    local active = { candidate = candidate, resources = resources, inited = {} }
    local exports = {}
    local ok, init_error = pcall(function()
        for index = 1, #candidate.graph.order do
            local module_id = candidate.graph.order[index]
            local module = candidate.graph.modules[module_id]
            local run_ok, descriptor = pcall(chunks[module_id])
            SL.expect(run_ok, module_id .. " chunk failed: " .. SL.safe_text(descriptor))
            if SL.has_capability(module, "full_env") and type(descriptor) ~= "table" then
                -- signed trusted payload: the chunk body is the script itself
                exports[module_id] = {}
                active.inited[#active.inited + 1] = { id = module_id, descriptor = {}, ctx = nil }
            else
            SL.expect(type(descriptor) == "table" and type(descriptor.init) == "function",
                module_id .. " must return a descriptor with an init function")
            local ctx = {
                module_id = module_id,
                module_version = module.version,
                log = function(message)
                    SL.log("info", module_id .. ": " .. SL.safe_text(message, 200))
                end,
                dependencies = {},
            }
            for capability_index = 1, #module.capabilities do
                local name = module.capabilities[capability_index]
                if name ~= "full_env" then
                    ctx[name] = SL.make_capability(name, resources)
                end
            end
            for dependency_index = 1, #module.dependencies do
                local dependency_id = module.dependencies[dependency_index]
                ctx.dependencies[dependency_id] = exports[dependency_id]
            end
            local init_ok, result = pcall(descriptor.init, ctx)
            SL.expect(init_ok, module_id .. " init failed: " .. SL.safe_text(result))
            exports[module_id] = type(result) == "table" and result or {}
            active.inited[#active.inited + 1] = { id = module_id, descriptor = descriptor, ctx = ctx }
            end
        end
    end)
    if not ok then
        SL.cleanup_bundle(active)
        return nil, init_error
    end
    return active
end

function SL.activate_candidate(generation, candidate)
    if generation ~= SL.state.generation or SL.state.shutdown then
        return
    end
    if SL.state.active ~= nil then
        SL.fail_attempt(generation, "a bundle is already active; reload the loader to update", "verification")
        return
    end
    SL.set_phase("compile", "compiling " .. #candidate.graph.order .. " modules from " .. candidate.source)
    local compiled, chunks_or_error = pcall(SL.compile_candidate, candidate)
    if not compiled then
        SL.fail_attempt(generation, chunks_or_error, "verification")
        return
    end
    SL.set_phase("initialize", "initializing modules in dependency order")
    local active, init_error = SL.initialize_candidate(candidate, chunks_or_error)
    if not active then
        SL.fail_attempt(generation, init_error, candidate.source == "network" and "network" or "verification")
        return
    end
    SL.state.active = active
    SL.state.verified_candidate = nil
    SL.state.failure_category = nil
    if candidate.source == "network" then
        local promoted, promote_error = SL.promote_candidate(candidate)
        if promoted ~= true then
            SL.log("error", promote_error)
        end
    end
    SL.set_phase("active", candidate.source .. " bundle: targets v" .. candidate.targets.signed.version
        .. ", epoch " .. candidate.targets.signed.security_epoch
        .. ", signers " .. table.concat(candidate.targets.valid_signers, ",")
        .. ", " .. #candidate.graph.module_ids .. " modules")
end

function SL.try_cache_fallback(generation)
    if not SL.database_available() then
        return false
    end
    local ok_state, row = pcall(database.read, SL.db_key("state"))
    if not ok_state or type(row) ~= "table" then
        return false
    end
    local first = row.active_slot == "b" and "b" or "a"
    local slots = { first, first == "a" and "b" or "a" }
    for index = 1, 2 do
        pcall(function()
            local slot = database.read(SL.db_key("slot_" .. slots[index]))
            local candidate = SL.verify_offline_bundle(slot, SL.now())
            SL.activate_candidate(generation, candidate)
        end)
        if SL.state.active ~= nil then
            return true
        end
    end
    return false
end

function SL.fail_attempt(generation, message, category)
    if generation ~= SL.state.generation or SL.state.shutdown then
        return
    end
    SL.state.request = nil
    SL.state.verified_candidate = nil
    SL.state.failure_category = category or "verification"
    if category == "network" and not SL.state.cache_attempted and SL.state.active == nil then
        SL.state.cache_attempted = true
        SL.log("error", "network attempt failed (" .. SL.safe_text(message, 200) .. "); trying verified cache")
        local ok, used = pcall(SL.try_cache_fallback, generation)
        if ok and used then
            return
        end
    end
    SL.set_phase("failed", SL.safe_text(message, 300), "error")
end

function SL.setup_ui()
    if type(ui) ~= "table" or type(ui.new_button) ~= "function" then
        return
    end
    pcall(ui.new_button, "LUA", "B", "Shinymoon loader: status / retry", function()
        local detail = SL.state.detail ~= "" and (": " .. SL.state.detail) or ""
        SL.log("info", "phase " .. SL.state.phase .. detail)
        local active = SL.state.active
        if active ~= nil then
            local graph = active.candidate.graph
            for index = 1, #graph.module_ids do
                local module = graph.modules[graph.module_ids[index]]
                SL.log("info", "module " .. module.id .. " v" .. module.version
                    .. " " .. module.sha256:sub(1, 12))
            end
            return
        end
        if SL.state.shutdown or SL.state.phase ~= "failed" then
            return
        end
        if SL.state.retries >= SL.limits.max_retries then
            SL.log("error", "retry limit reached; reload the loader")
            return
        end
        SL.state.retries = SL.state.retries + 1
        local started, start_error = SL.start_network_attempt()
        if not started then
            SL.log("error", "retry failed to start: " .. SL.safe_text(start_error))
        end
    end)
end

function SL.load_http()
    if SL.http then
        return true
    end
    if type(client) ~= "table" or type(client.delay_call) ~= "function" then
        return nil, "client.delay_call is unavailable"
    end
    local ok, module_or_error = pcall(require, "gamesense/http")
    if not ok then
        return nil, "gamesense/http unavailable (unsafe scripts and the library are required): "
            .. SL.safe_text(module_or_error)
    end
    if type(module_or_error) ~= "table" or type(module_or_error.get) ~= "function" then
        return nil, "gamesense/http does not expose get(url, callback)"
    end
    SL.http = module_or_error
    return true
end

function SL.fetch_one(generation, url, maximum, label, callback)
    if generation ~= SL.state.generation or SL.state.shutdown then
        return
    end
    SL.expect(SL.state.request == nil, "network pipeline attempted concurrent requests")
    SL.assert_ascii(url, label .. " URL", 1, 600)
    SL.state.request_serial = SL.state.request_serial + 1
    local request = { generation = generation, id = SL.state.request_serial, done = false, label = label }
    SL.state.request = request

    local function current()
        return not request.done and not SL.state.shutdown and generation == SL.state.generation
            and SL.state.request == request
    end
    local function finish()
        if not current() then
            return false
        end
        request.done = true
        SL.state.request = nil
        return true
    end

    local delay_ok, delay_error = pcall(client.delay_call, SL.limits.request_timeout, function()
        if finish() then
            SL.fail_attempt(generation, label .. " request timed out", "network")
        end
    end)
    if not delay_ok then
        request.done = true
        SL.state.request = nil
        SL.fail_attempt(generation, label .. " deadline scheduling failed: " .. tostring(delay_error), "network")
        return
    end

    local get_ok, get_error = pcall(SL.http.get, url, function(success, response)
        if not finish() then
            return
        end
        if success ~= true or type(response) ~= "table" or response.status ~= 200 then
            local status = type(response) == "table" and response.status or "transport"
            SL.fail_attempt(generation, label .. " request failed with status " .. tostring(status), "network")
            return
        end
        if type(response.body) ~= "string" then
            SL.fail_attempt(generation, label .. " response body is not bytes", "verification")
            return
        end
        if #response.body < 1 or #response.body > maximum then
            SL.fail_attempt(generation, label .. " response size is outside bounds", "verification")
            return
        end
        local callback_ok, callback_error = pcall(callback, response.body)
        if not callback_ok then
            SL.fail_attempt(generation, callback_error, "verification")
        end
    end)
    if not get_ok and finish() then
        SL.fail_attempt(generation, label .. " request setup failed: " .. tostring(get_error), "network")
    end
end

function SL.construct_module_url(module)
    SL.valid_owner_repo(module.owner, module.id .. ".owner")
    SL.valid_owner_repo(module.repo, module.id .. ".repo")
    SL.expect(type(module.commit) == "string" and #module.commit == 40
        and module.commit:match("^[0-9a-f]+$") ~= nil, module.id .. " commit pin is invalid")
    SL.validate_module_path(module.path, module.id .. ".path", false)
    return "https://raw.githubusercontent.com/" .. module.owner .. "/" .. module.repo .. "/"
        .. module.commit .. "/" .. module.path
end

function SL.accept_verified_candidate(generation, candidate)
    if generation ~= SL.state.generation or SL.state.shutdown then
        return
    end
    local final_now = SL.now()
    SL.check_expected_common(candidate.timestamp, final_now)
    SL.check_expected_common(candidate.snapshot, final_now)
    SL.check_expected_common(candidate.targets, final_now)
    SL.state.verified_candidate = candidate
    SL.state.failure_category = nil
    local targets = candidate.targets
    SL.set_phase("candidate_verified", "targets v" .. targets.signed.version .. ", epoch "
        .. targets.signed.security_epoch .. ", " .. #candidate.graph.module_ids .. " modules")
    SL.activate_candidate(generation, candidate)
end

function SL.fetch_candidate_modules(generation, candidate, index)
    if generation ~= SL.state.generation or SL.state.shutdown then
        return
    end
    if index > #candidate.graph.module_ids then
        candidate.high_water = SL.make_candidate_high_water(candidate)
        SL.accept_verified_candidate(generation, candidate)
        return
    end
    local module_id = candidate.graph.module_ids[index]
    local module = candidate.graph.modules[module_id]
    local url = SL.construct_module_url(module)
    SL.set_phase("fetch_module", module_id .. " (" .. index .. "/" .. #candidate.graph.module_ids .. ")")
    SL.fetch_one(generation, url, module.length, "module " .. module_id, function(raw)
        SL.expect(#raw == module.length, module_id .. " byte length mismatch")
        local digest, digest_error = SL.sha256_hex(raw)
        SL.expect(digest ~= nil, digest_error)
        SL.expect(digest == module.sha256, module_id .. " SHA-256 mismatch")
        candidate.module_bytes[module_id] = raw
        SL.fetch_candidate_modules(generation, candidate, index + 1)
    end)
end

function SL.fetch_metadata_chain(generation)
    local now = SL.now()
    local base = SL.config.metadata_base_url
    SL.set_phase("fetch_timestamp", "requesting timestamp.json")
    SL.fetch_one(generation, base .. "/timestamp.json", SL.limits.metadata_bytes, "timestamp", function(timestamp_raw)
        local timestamp = SL.verify_current_role(timestamp_raw, "timestamp", now)
        local candidate = { timestamp = timestamp, timestamp_raw = timestamp_raw, module_bytes = {}, source = "network" }
        SL.set_phase("fetch_snapshot", "timestamp verified by " .. table.concat(timestamp.valid_signers, ","))
        SL.fetch_one(generation, base .. "/snapshot.json", SL.limits.metadata_bytes, "snapshot", function(snapshot_raw)
            SL.assert_raw_reference(snapshot_raw, timestamp.signed.snapshot, "snapshot")
            local snapshot = SL.verify_current_role(snapshot_raw, "snapshot", now)
            SL.expect(snapshot.signed.version == timestamp.signed.snapshot.version, "timestamp snapshot version mismatch")
            candidate.snapshot = snapshot
            candidate.snapshot_raw = snapshot_raw
            SL.set_phase("fetch_targets", "snapshot verified by " .. table.concat(snapshot.valid_signers, ","))
            SL.fetch_one(generation, base .. "/targets.json", SL.limits.metadata_bytes, "targets", function(targets_raw)
                SL.assert_raw_reference(targets_raw, snapshot.signed.targets, "targets")
                local targets = SL.verify_current_role(targets_raw, "targets", now)
                SL.expect(targets.signed.version == snapshot.signed.targets.version, "snapshot targets version mismatch")
                candidate.targets = targets
                candidate.targets_raw = targets_raw
                candidate.graph = targets.graph
                SL.check_chain_epochs_and_floor(candidate)
                local total = 0
                for module_index = 1, #candidate.graph.module_ids do
                    total = total + candidate.graph.modules[candidate.graph.module_ids[module_index]].length
                end
                SL.expect(total <= SL.limits.bundle_bytes, "declared bundle size exceeds the limit")
                candidate.total_bytes = total
                SL.fetch_candidate_modules(generation, candidate, 1)
            end)
        end)
    end)
end

function SL.start_network_attempt()
    if SL.state.shutdown then
        return nil, "loader is shut down"
    end
    if SL.state.active ~= nil then
        return nil, "a bundle is already active; reload the loader to update"
    end
    if SL.state.request ~= nil then
        return nil, "a load attempt is already in flight"
    end
    SL.state.generation = SL.state.generation + 1
    local generation = SL.state.generation
    SL.state.failure_category = nil
    SL.state.verified_candidate = nil
    SL.state.cache_attempted = false
    local loaded, load_error = SL.load_http()
    if not loaded then
        SL.fail_attempt(generation, load_error, "network")
        return nil, load_error
    end
    local ok, start_error = pcall(SL.fetch_metadata_chain, generation)
    if not ok then
        SL.fail_attempt(generation, start_error, "verification")
        return nil, start_error
    end
    return true
end

function SL.shutdown()
    if SL.state.shutdown then
        return
    end
    SL.state.shutdown = true
    SL.state.generation = SL.state.generation + 1
    SL.state.request = nil
    SL.state.verified_candidate = nil
    local active = SL.state.active
    SL.state.active = nil
    pcall(SL.cleanup_bundle, active)
    SL.http = nil
    SL.crypto_close()
    SL.state.phase = "shutdown"
end

function SL.bootstrap_foundation()
    SL.set_phase("crypto_init", "initializing Windows CNG")
    local opened, open_error = SL.crypto_open()
    if not opened then
        SL.set_phase("failed", open_error, "error")
        return
    end
    local tested, test_error = SL.crypto_self_test()
    if not tested then
        SL.crypto_close()
        SL.set_phase("failed", test_error, "error")
        return
    end
    local canonical, canonical_error = SL.canonical_self_test()
    if not canonical then
        SL.crypto_close()
        SL.set_phase("failed", canonical_error, "error")
        return
    end
    local configured, config_error = SL.config_foundation_valid()
    if not configured then
        SL.crypto_close()
        SL.set_phase("configuration_required", config_error, "error")
        return
    end
    SL.set_phase("foundation_ready", "CNG self-tests and public trust shape passed")
    SL.load_persisted_high_water()
    SL.setup_ui()
    SL.start_network_attempt()
end

if type(client) == "table" and type(client.set_event_callback) == "function" then
    pcall(client.set_event_callback, "shutdown", SL.shutdown)
end

SL.bootstrap_foundation()
