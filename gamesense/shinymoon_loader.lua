-- ECDSA P-256 verification over pure-Lua big integers (base 2^24 limbs).
-- ponytail: bn_mod is bit-at-a-time long division. Correct but not fast; each
-- verify is a few thousand mulmods. Runs a handful of times at load, so the
-- cost is acceptable. Upgrade path if latency bites: NIST P-256 Solinas
-- fast-reduction to replace the general division in the mod-p hot path.
do
    local BASE = 16777216 -- 2^24
    local floor = math.floor

    local function bn_norm(a)
        local n = #a
        while n > 1 and a[n] == 0 do a[n] = nil; n = n - 1 end
        return a
    end
    local function bn_copy(a)
        local r = {}
        for i = 1, #a do r[i] = a[i] end
        return r
    end
    local function bn_zero() return { 0 } end
    local function bn_is_zero(a) return #a == 1 and a[1] == 0 end
    local function bn_from_int(x)
        if x == 0 then return { 0 } end
        local r = {}
        while x > 0 do r[#r + 1] = x % BASE; x = floor(x / BASE) end
        return r
    end
    local ONE = bn_from_int(1)
    local TWO = bn_from_int(2)
    local C256 = bn_from_int(256)
    local C3 = bn_from_int(3)
    local C4 = bn_from_int(4)
    local C8 = bn_from_int(8)

    local function bn_cmp(a, b)
        if #a ~= #b then return #a < #b and -1 or 1 end
        for i = #a, 1, -1 do
            if a[i] ~= b[i] then return a[i] < b[i] and -1 or 1 end
        end
        return 0
    end
    local function bn_add(a, b)
        local r = {}
        local carry = 0
        local n = #a > #b and #a or #b
        for i = 1, n do
            local s = (a[i] or 0) + (b[i] or 0) + carry
            if s >= BASE then r[i] = s - BASE; carry = 1 else r[i] = s; carry = 0 end
        end
        if carry > 0 then r[n + 1] = carry end
        return r
    end
    local function bn_sub(a, b) -- assumes a >= b
        local r = {}
        local borrow = 0
        for i = 1, #a do
            local s = a[i] - (b[i] or 0) - borrow
            if s < 0 then r[i] = s + BASE; borrow = 1 else r[i] = s; borrow = 0 end
        end
        return bn_norm(r)
    end
    local function bn_shl1(a)
        local r = {}
        local carry = 0
        for i = 1, #a do
            local s = a[i] * 2 + carry
            if s >= BASE then r[i] = s - BASE; carry = 1 else r[i] = s; carry = 0 end
        end
        if carry > 0 then r[#a + 1] = carry end
        return r
    end
    local function bn_mul(a, b)
        local r = {}
        for i = 1, #a + #b do r[i] = 0 end
        for i = 1, #a do
            local carry = 0
            local ai = a[i]
            for j = 1, #b do
                local idx = i + j - 1
                local s = r[idx] + ai * b[j] + carry
                carry = floor(s / BASE)
                r[idx] = s - carry * BASE
            end
            r[i + #b] = r[i + #b] + carry
        end
        return bn_norm(r)
    end
    local function bn_bitlen(a)
        local top = a[#a]
        local bits = (#a - 1) * 24
        while top > 0 do bits = bits + 1; top = floor(top / 2) end
        return bits
    end
    local function bn_testbit(a, i)
        local limb = floor(i / 24) + 1
        if limb > #a then return 0 end
        return floor(a[limb] / 2 ^ (i % 24)) % 2
    end
    local function bn_mod(a, m)
        if bn_cmp(a, m) < 0 then return bn_copy(a) end
        local r = bn_zero()
        for i = bn_bitlen(a) - 1, 0, -1 do
            r = bn_shl1(r)
            if bn_testbit(a, i) == 1 then r[1] = r[1] + 1 end
            if bn_cmp(r, m) >= 0 then r = bn_sub(r, m) end
        end
        return bn_norm(r)
    end
    local function bn_mulmod(a, b, m) return bn_mod(bn_mul(a, b), m) end
    local function bn_addmod(a, b, m)
        local s = bn_add(a, b)
        if bn_cmp(s, m) >= 0 then s = bn_sub(s, m) end
        return s
    end
    local function bn_submod(a, b, m)
        if bn_cmp(a, b) >= 0 then return bn_sub(a, b) end
        return bn_sub(bn_add(a, m), b)
    end
    local function bn_modpow(base, e, m)
        local result = bn_from_int(1)
        base = bn_mod(base, m)
        for i = 0, bn_bitlen(e) - 1 do
            if bn_testbit(e, i) == 1 then result = bn_mulmod(result, base, m) end
            base = bn_mulmod(base, base, m)
        end
        return result
    end
    local function bn_invmod(a, m) return bn_modpow(a, bn_sub(m, TWO), m) end
    local function bn_from_bytes(s)
        local r = bn_zero()
        for i = 1, #s do
            r = bn_add(bn_mul(r, C256), bn_from_int(s:byte(i)))
        end
        return bn_norm(r)
    end
    local HEXVAL = {}
    for i = 0, 9 do HEXVAL[string.char(48 + i)] = i end
    for i = 0, 5 do HEXVAL[string.char(97 + i)] = 10 + i end
    local function bn_from_hex(h)
        if type(h) ~= "string" or #h % 2 ~= 0 then return nil end
        local bytes = {}
        for i = 1, #h, 2 do
            local a = HEXVAL[h:sub(i, i)]
            local b = HEXVAL[h:sub(i + 1, i + 1)]
            if not a or not b then return nil end
            bytes[#bytes + 1] = string.char(a * 16 + b)
        end
        return bn_from_bytes(table.concat(bytes))
    end

    local P = bn_from_hex("ffffffff00000001000000000000000000000000ffffffffffffffffffffffff")
    local N = bn_from_hex("ffffffff00000000ffffffffffffffffbce6faada7179e84f3b9cac2fc632551")
    local Bcurve = bn_from_hex("5ac635d8aa3a93e7b3ebbd55769886bc651d06b0cc53b0f63bce3c3e27d2604b")
    local GX = bn_from_hex("6b17d1f2e12c4247f8bce6e563a440f277037d812deb33a0f4a13945d898c296")
    local GY = bn_from_hex("4fe342e2fe1a7f9b8ee7eb4a7c0f9e162bce33576b315ececbb6406837bf51f5")

    local function fadd(a, b) return bn_addmod(a, b, P) end
    local function fsub(a, b) return bn_submod(a, b, P) end
    local function fmul(a, b) return bn_mulmod(a, b, P) end
    local INFINITY = { X = ONE, Y = ONE, Z = bn_zero() }

    local function jac_double(pt)
        if bn_is_zero(pt.Z) then return pt end
        local X, Y, Z = pt.X, pt.Y, pt.Z
        local A = fmul(Y, Y)
        local Bc = fmul(fmul(C4, X), A)
        local C = fmul(fmul(C8, A), A)
        local Z2 = fmul(Z, Z)
        local D = fmul(C3, fmul(fsub(X, Z2), fadd(X, Z2)))
        local X3 = fsub(fmul(D, D), fadd(Bc, Bc))
        local Y3 = fsub(fmul(D, fsub(Bc, X3)), C)
        local Z3 = fmul(fmul(TWO, Y), Z)
        return { X = X3, Y = Y3, Z = Z3 }
    end
    local function jac_add(p1, p2)
        if bn_is_zero(p1.Z) then return p2 end
        if bn_is_zero(p2.Z) then return p1 end
        local X1, Y1, Z1 = p1.X, p1.Y, p1.Z
        local X2, Y2, Z2 = p2.X, p2.Y, p2.Z
        local Z1Z1 = fmul(Z1, Z1)
        local Z2Z2 = fmul(Z2, Z2)
        local U1 = fmul(X1, Z2Z2)
        local U2 = fmul(X2, Z1Z1)
        local S1 = fmul(Y1, fmul(Z2, Z2Z2))
        local S2 = fmul(Y2, fmul(Z1, Z1Z1))
        if bn_cmp(U1, U2) == 0 then
            if bn_cmp(S1, S2) ~= 0 then return INFINITY end
            return jac_double(p1)
        end
        local H = fsub(U2, U1)
        local R = fsub(S2, S1)
        local HH = fmul(H, H)
        local HHH = fmul(H, HH)
        local U1HH = fmul(U1, HH)
        local X3 = fsub(fsub(fmul(R, R), HHH), fadd(U1HH, U1HH))
        local Y3 = fsub(fmul(R, fsub(U1HH, X3)), fmul(S1, HHH))
        local Z3 = fmul(fmul(Z1, Z2), H)
        return { X = X3, Y = Y3, Z = Z3 }
    end
    local function jac_mul(k, pt)
        local result = INFINITY
        for i = bn_bitlen(k) - 1, 0, -1 do
            result = jac_double(result)
            if bn_testbit(k, i) == 1 then result = jac_add(result, pt) end
        end
        return result
    end
    local function on_curve(x, y)
        local left = fmul(y, y)
        local right = fadd(fsub(fmul(fmul(x, x), x), fmul(C3, x)), Bcurve)
        return bn_cmp(left, right) == 0
    end

    -- message: bytes to hash; signature_base64: P1363 r||s; x_hex/y_hex: public key.
    function SL.verify_p256(message, signature_base64, x_hex, y_hex)
        if type(message) ~= "string" then
            return nil, "signature message must be a string"
        end
        local signature, signature_error = SL.base64_decode(signature_base64, 64)
        if not signature then
            return nil, signature_error
        end
        local qx = bn_from_hex(x_hex)
        local qy = bn_from_hex(y_hex)
        if not qx or not qy then
            return nil, "public key hex is malformed"
        end
        local hash, hash_error = SL.sha256_raw(message)
        if not hash then
            return nil, hash_error
        end
        local r = bn_from_bytes(signature:sub(1, 32))
        local s = bn_from_bytes(signature:sub(33, 64))
        if bn_is_zero(r) or bn_cmp(r, N) >= 0 then return false, "signature r out of range" end
        if bn_is_zero(s) or bn_cmp(s, N) >= 0 then return false, "signature s out of range" end
        if bn_cmp(qx, P) >= 0 or bn_cmp(qy, P) >= 0 then return nil, "public key coordinate out of range" end
        if not on_curve(qx, qy) then return nil, "public key is not on the curve" end
        local e = bn_mod(bn_from_bytes(hash), N)
        local w = bn_invmod(s, N)
        local u1 = bn_mulmod(e, w, N)
        local u2 = bn_mulmod(r, w, N)
        local point = jac_add(jac_mul(u1, { X = GX, Y = GY, Z = ONE }),
            jac_mul(u2, { X = qx, Y = qy, Z = ONE }))
        if bn_is_zero(point.Z) then return false, "signature reduces to point at infinity" end
        local zinv = bn_invmod(point.Z, P)
        local x = fmul(point.X, fmul(zinv, zinv))
        if bn_cmp(bn_mod(x, N), r) == 0 then
            return true
        end
        return false, "signature does not verify"
    end
end

function SL.crypto_self_test()
    local abc, hash_error = SL.sha256_hex("abc")
    if abc ~= SL.constants.sha256_abc then
        return nil, hash_error or "SHA256 known-answer mismatch"
    end
    local valid, verify_error = SL.verify_p256(
        SL.constants.self_test_message,
        SL.constants.self_test_signature,
        SL.constants.self_test_x,
        SL.constants.self_test_y
    )
    if valid ~= true then
        return nil, verify_error or "ECDSA known-answer verification failed"
    end
    local tampered, tamper_error = SL.verify_p256(
        SL.constants.self_test_message .. "!",
        SL.constants.self_test_signature,
        SL.constants.self_test_x,
        SL.constants.self_test_y
    )
    if tampered ~= false then
        return nil, tamper_error or "ECDSA tamper self-test did not reject"
    end
    return true
end

function SL.raise(message)
    error(SL.safe_text(message, 360), 0)
end

function SL.expect(condition, message)
    if not condition then
        SL.raise(message)
    end
    return condition
end

function SL.assert_ascii(value, label, minimum, maximum)
    SL.expect(type(value) == "string", label .. " must be a string")
    SL.expect(#value >= (minimum or 1) and #value <= (maximum or 256), label .. " length is outside bounds")
    for index = 1, #value do
        local byte = value:byte(index)
        SL.expect(byte >= 0x20 and byte <= 0x7e, label .. " must be printable ASCII")
    end
    return value
end

function SL.assert_integer(value, label, minimum, maximum)
    local upper = maximum or 9007199254740991
    SL.expect(type(value) == "number" and value == math.floor(value), label .. " must be an integer")
    SL.expect(value >= (minimum or 0) and value <= upper, label .. " is outside integer bounds")
    return value
end

function SL.integer_text(value)
    SL.assert_integer(value, "canonical integer", 0)
    return string.format("%.0f", value)
end

function SL.valid_id(value, label)
    SL.assert_ascii(value, label, 1, 64)
    SL.expect(value:match("^[a-z][a-z0-9_.%-]*$") ~= nil, label .. " has an invalid identifier")
    return value
end

function SL.valid_owner_repo(value, label)
    SL.assert_ascii(value, label, 1, 100)
    SL.expect(value:match("^[A-Za-z0-9_.%-]+$") ~= nil, label .. " has an invalid owner/repository name")
    return value
end

function SL.valid_digest(value, label)
    SL.expect(type(value) == "string" and #value == 64 and value:match("^[0-9a-f]+$") ~= nil,
        label .. " must be a lowercase SHA-256 digest")
    return value
end

function SL.exact_keys(value, expected, label)
    SL.expect(type(value) == "table", label .. " must be an object")
    SL.expect(SL.json_kind[value] == nil or SL.json_kind[value] == "object", label .. " must not be an array")
    local allowed = {}
    for index = 1, #expected do
        allowed[expected[index]] = true
        SL.expect(value[expected[index]] ~= nil, label .. " is missing " .. expected[index])
    end
    local count = 0
    for key in pairs(value) do
        count = count + 1
        SL.expect(type(key) == "string" and allowed[key], label .. " has an unexpected key")
    end
    SL.expect(count == #expected, label .. " key count is invalid")
    return value
end

function SL.array_length(value, label, minimum, maximum)
    SL.expect(type(value) == "table", label .. " must be an array")
    SL.expect(SL.json_kind[value] == nil or SL.json_kind[value] == "array", label .. " must not be an object")
    local count = 0
    local highest = 0
    for key in pairs(value) do
        SL.expect(type(key) == "number" and key == math.floor(key) and key >= 1, label .. " has a non-array key")
        count = count + 1
        if key > highest then
            highest = key
        end
    end
    SL.expect(highest == count, label .. " must not contain holes")
    SL.expect(count >= (minimum or 0) and count <= (maximum or count), label .. " length is outside bounds")
    return count
end

function SL.sorted_unique_strings(value, label, validator, maximum)
    local count = SL.array_length(value, label, 0, maximum or 64)
    local out = {}
    local seen = {}
    for index = 1, count do
        local item = validator(value[index], label .. "[" .. index .. "]")
        SL.expect(not seen[item], label .. " contains duplicate " .. item)
        seen[item] = true
        out[index] = item
    end
    table.sort(out)
    return out
end

function SL.strict_json_parse(raw, label, maximum)
    SL.expect(type(raw) == "string", label .. " must be bytes")
    SL.expect(#raw >= 2 and #raw <= (maximum or SL.limits.metadata_bytes), label .. " byte length is outside bounds")
    for index = 1, #raw do
        local byte = raw:byte(index)
        SL.expect(byte == 0x09 or byte == 0x0a or byte == 0x0d or (byte >= 0x20 and byte <= 0x7e),
            label .. " must contain ASCII JSON bytes only")
    end

    local offset = 1
    local function fail_here(message)
        SL.raise(label .. " " .. message .. " at byte " .. (offset - 1))
    end
    local function whitespace()
        while offset <= #raw do
            local byte = raw:byte(offset)
            if byte ~= 0x09 and byte ~= 0x0a and byte ~= 0x0d and byte ~= 0x20 then
                break
            end
            offset = offset + 1
        end
    end
    local function parse_string()
        if raw:sub(offset, offset) ~= '"' then
            fail_here("expected a string")
        end
        offset = offset + 1
        local out = {}
        while offset <= #raw do
            local byte = raw:byte(offset)
            if byte == 0x22 then
                offset = offset + 1
                return table.concat(out)
            end
            if byte == 0x5c then
                offset = offset + 1
                local escaped = raw:sub(offset, offset)
                local simple = { ['"'] = '"', ["\\"] = "\\", ["/"] = "/" }
                if simple[escaped] then
                    out[#out + 1] = simple[escaped]
                    offset = offset + 1
                elseif escaped == "u" then
                    local hex = raw:sub(offset + 1, offset + 4)
                    if #hex ~= 4 or hex:match("^[0-9a-fA-F]+$") == nil then
                        fail_here("has an invalid unicode escape")
                    end
                    local decoded = tonumber(hex, 16)
                    if decoded < 0x20 or decoded > 0x7e then
                        fail_here("string decodes outside printable ASCII")
                    end
                    out[#out + 1] = string.char(decoded)
                    offset = offset + 5
                else
                    fail_here("string escape is not printable ASCII")
                end
            else
                if byte < 0x20 or byte > 0x7e then
                    fail_here("string contains a non-printable byte")
                end
                out[#out + 1] = string.char(byte)
                offset = offset + 1
            end
        end
        fail_here("has an unterminated string")
    end
    local function parse_number()
        local start = offset
        if raw:sub(offset, offset) == "-" then
            offset = offset + 1
        end
        local first = raw:sub(offset, offset)
        if first == "0" then
            offset = offset + 1
            if raw:sub(offset, offset):match("%d") then
                fail_here("has a leading-zero number")
            end
        elseif first:match("[1-9]") then
            repeat
                offset = offset + 1
            until raw:sub(offset, offset):match("%d") == nil
        else
            fail_here("has an invalid number")
        end
        local suffix = raw:sub(offset, offset)
        if suffix == "." or suffix == "e" or suffix == "E" then
            fail_here("uses a non-integer number")
        end
        local token = raw:sub(start, offset - 1)
        if token == "-0" then
            fail_here("uses negative zero")
        end
        local number = tonumber(token)
        if not number or math.abs(number) > 9007199254740991 then
            fail_here("integer is outside the safe range")
        end
        return number
    end

    local parse_value
    parse_value = function(depth)
        if depth > SL.limits.json_depth then
            fail_here("exceeds JSON depth " .. SL.limits.json_depth)
        end
        whitespace()
        local first = raw:sub(offset, offset)
        if first == "{" then
            offset = offset + 1
            whitespace()
            local object = {}
            SL.json_kind[object] = "object"
            if raw:sub(offset, offset) == "}" then
                offset = offset + 1
                return object
            end
            while true do
                local key = parse_string()
                if object[key] ~= nil then
                    fail_here("has duplicate object key " .. key)
                end
                whitespace()
                if raw:sub(offset, offset) ~= ":" then
                    fail_here("expected ':'")
                end
                offset = offset + 1
                object[key] = parse_value(depth + 1)
                whitespace()
                local delimiter = raw:sub(offset, offset)
                if delimiter == "}" then
                    offset = offset + 1
                    return object
                end
                if delimiter ~= "," then
                    fail_here("expected ',' or '}'")
                end
                offset = offset + 1
                whitespace()
            end
        end
        if first == "[" then
            offset = offset + 1
            whitespace()
            local array = {}
            SL.json_kind[array] = "array"
            if raw:sub(offset, offset) == "]" then
                offset = offset + 1
                return array
            end
            while true do
                array[#array + 1] = parse_value(depth + 1)
                whitespace()
                local delimiter = raw:sub(offset, offset)
                if delimiter == "]" then
                    offset = offset + 1
                    return array
                end
                if delimiter ~= "," then
                    fail_here("expected ',' or ']'")
                end
                offset = offset + 1
                whitespace()
            end
        end
        if first == '"' then
            return parse_string()
        end
        if first == "t" and raw:sub(offset, offset + 3) == "true" then
            offset = offset + 4
            return true
        end
        if first == "f" and raw:sub(offset, offset + 4) == "false" then
            offset = offset + 5
            return false
        end
        if first == "n" and raw:sub(offset, offset + 3) == "null" then
            fail_here("uses unsupported null")
        end
        return parse_number()
    end

    whitespace()
    local value = parse_value(1)
    whitespace()
    if offset <= #raw then
        fail_here("has trailing data")
    end
    return value
end

function SL.validate_metadata_base_url(value)
    SL.assert_ascii(value, "metadata_base_url", 1, 400)
    SL.expect(value:sub(-1) ~= "/" and not value:find("%%") and not value:find("\\", 1, true)
        and not value:find("?", 1, true) and not value:find("#", 1, true),
        "metadata_base_url is not normalized")
    local path = value:match("^https://raw%.githubusercontent%.com/(.+)$")
    SL.expect(path ~= nil, "metadata_base_url must use plain HTTPS raw.githubusercontent.com")
    local segments = {}
    for segment in path:gmatch("[^/]+") do
        segments[#segments + 1] = segment
    end
    SL.expect(#segments >= 4 and table.concat(segments, "/") == path,
        "metadata_base_url requires owner/repo/ref/path without empty segments")
    for index = 1, #segments do
        SL.expect(segments[index] ~= "." and segments[index] ~= ".."
            and segments[index]:match("^[A-Za-z0-9_.%-]+$") ~= nil,
            "metadata_base_url has an unsafe path segment")
    end
    return value
end

function SL.validate_module_path(value, label, prefix)
    SL.assert_ascii(value, label, 1, prefix and 200 or 240)
    SL.expect(value:sub(1, 1) ~= "/" and not value:find("//", 1, true)
        and not value:find("\\", 1, true) and not value:find("%%")
        and not value:find("?", 1, true) and not value:find("#", 1, true),
        label .. " has a forbidden path character")
    if prefix then
        SL.expect(value:sub(-1) == "/", label .. " must end with '/'")
        value = value:sub(1, -2)
    else
        SL.expect(value:sub(-4) == ".lua" and value:sub(-1) ~= "/", label .. " must name a .lua file")
    end
    SL.expect(#value > 0, label .. " must not be empty")
    for segment in value:gmatch("[^/]+") do
        SL.expect(segment ~= "." and segment ~= ".." and segment:match("^[A-Za-z0-9_.%-]+$") ~= nil,
            label .. " has an unsafe segment")
    end
    return prefix and (value .. "/") or value
end

function SL.validate_allowed_targets(value)
    local count = SL.array_length(value, "allowed_targets", 1, 16)
    local result = {}
    local seen = {}
    for index = 1, count do
        local rule = SL.exact_keys(value[index], { "owner", "path_prefix", "repo" }, "allowed_targets[" .. index .. "]")
        local owner = SL.valid_owner_repo(rule.owner, "allowed target owner")
        local repo = SL.valid_owner_repo(rule.repo, "allowed target repo")
        local path_prefix = SL.validate_module_path(rule.path_prefix, "allowed target path_prefix", true)
        local identity = owner .. "/" .. repo .. "/" .. path_prefix
        SL.expect(not seen[identity], "duplicate allowed target " .. identity)
        seen[identity] = true
        result[index] = { owner = owner, repo = repo, path_prefix = path_prefix }
    end
    return result
end

function SL.validate_meta_ref(value, label)
    SL.exact_keys(value, { "length", "sha256", "version" }, label)
    SL.assert_integer(value.version, label .. ".version", 1)
    SL.assert_integer(value.length, label .. ".length", 1, 1024 * 1024)
    SL.valid_digest(value.sha256, label .. ".sha256")
    return value
end

function SL.validate_common(signed, role)
    SL.assert_integer(signed.schema, role .. ".schema", 1)
    SL.expect(signed.schema == 1, role .. ".schema must be 1")
    SL.expect(signed.role == role, role .. ".role mismatch")
    SL.valid_id(signed.bundle_id, role .. ".bundle_id")
    SL.valid_id(signed.channel, role .. ".channel")
    SL.assert_integer(signed.version, role .. ".version", 0)
    SL.assert_integer(signed.security_epoch, role .. ".security_epoch", 0)
    SL.assert_integer(signed.issued_at, role .. ".issued_at", 0)
    SL.assert_integer(signed.expires_at, role .. ".expires_at", 0)
    SL.assert_integer(signed.min_loader_version, role .. ".min_loader_version", 0)
    SL.expect(signed.expires_at > signed.issued_at, role .. " expiry must follow issue time")
    SL.expect(signed.expires_at - signed.issued_at <= SL.limits.role_lifetime[role], role .. " lifetime is too long")
end

function SL.validate_targets(signed, allowed_targets)
    SL.exact_keys(signed, {
        "bundle_id", "channel", "entrypoints", "expires_at", "issued_at", "min_loader_version",
        "modules", "role", "rollback_floor", "schema", "security_epoch", "version",
    }, "targets.signed")
    SL.validate_common(signed, "targets")
    SL.assert_integer(signed.rollback_floor, "targets.rollback_floor", 0)
    SL.expect(signed.version >= signed.rollback_floor, "targets.version is below rollback_floor")
    local entrypoints = SL.sorted_unique_strings(signed.entrypoints, "targets.entrypoints", SL.valid_id, 32)
    local module_count = SL.array_length(signed.modules, "targets.modules", 1, SL.limits.module_count)
    local modules = {}
    local module_ids = {}
    local capability_allow = {
        client = true, cvar = true, database = true, entity = true, globals = true,
        json = true, plist = true, renderer = true, ui = true, full_env = true,
    }
    for index = 1, module_count do
        local source = SL.exact_keys(signed.modules[index], {
            "capabilities", "commit", "dependencies", "id", "length", "owner", "path",
            "repo", "sha256", "version",
        }, "targets.modules[" .. index .. "]")
        local module_id = SL.valid_id(source.id, "module id")
        SL.expect(modules[module_id] == nil, "duplicate module id " .. module_id)
        SL.assert_ascii(source.version, module_id .. ".version", 1, 64)
        SL.valid_owner_repo(source.owner, module_id .. ".owner")
        SL.valid_owner_repo(source.repo, module_id .. ".repo")
        SL.expect(type(source.commit) == "string" and #source.commit == 40
            and source.commit:match("^[0-9a-f]+$") ~= nil, module_id .. ".commit must be 40 lowercase hex")
        SL.validate_module_path(source.path, module_id .. ".path", false)
        SL.assert_integer(source.length, module_id .. ".length", 1, SL.limits.module_bytes)
        SL.valid_digest(source.sha256, module_id .. ".sha256")
        local dependencies = SL.sorted_unique_strings(source.dependencies, module_id .. ".dependencies", SL.valid_id, 32)
        local capabilities = SL.sorted_unique_strings(source.capabilities, module_id .. ".capabilities", function(value, label)
            SL.valid_id(value, label)
            SL.expect(capability_allow[value], label .. " is not an allowed capability")
            return value
        end, 10)
        for dependency_index = 1, #dependencies do
            SL.expect(dependencies[dependency_index] ~= module_id, module_id .. " depends on itself")
        end
        local allowed = false
        for rule_index = 1, #allowed_targets do
            local rule = allowed_targets[rule_index]
            if source.owner == rule.owner and source.repo == rule.repo
                and source.path:sub(1, #rule.path_prefix) == rule.path_prefix then
                allowed = true
                break
            end
        end
        SL.expect(allowed, module_id .. " is outside the configured target policy")
        modules[module_id] = {
            id = module_id, version = source.version, owner = source.owner, repo = source.repo,
            commit = source.commit, path = source.path, length = source.length, sha256 = source.sha256,
            dependencies = dependencies, capabilities = capabilities,
        }
        module_ids[#module_ids + 1] = module_id
    end
    table.sort(module_ids)
    for index = 1, #entrypoints do
        SL.expect(modules[entrypoints[index]] ~= nil, "entrypoint " .. entrypoints[index] .. " is missing")
    end
    for index = 1, #module_ids do
        local module = modules[module_ids[index]]
        for dependency_index = 1, #module.dependencies do
            SL.expect(modules[module.dependencies[dependency_index]] ~= nil,
                module.id .. " has missing dependency " .. module.dependencies[dependency_index])
        end
    end
    local visit_state = {}
    local order = {}
    local function visit(module_id, depth)
        SL.expect(depth <= SL.limits.dependency_depth, "dependency graph exceeds depth " .. SL.limits.dependency_depth)
        if visit_state[module_id] == 1 then
            SL.raise("dependency cycle includes " .. module_id)
        end
        if visit_state[module_id] == 2 then
            return
        end
        visit_state[module_id] = 1
        local module = modules[module_id]
        for dependency_index = 1, #module.dependencies do
            visit(module.dependencies[dependency_index], depth + 1)
        end
        visit_state[module_id] = 2
        order[#order + 1] = module_id
    end
    for index = 1, #module_ids do
        visit(module_ids[index], 1)
    end
    return { entrypoints = entrypoints, modules = modules, module_ids = module_ids, order = order }
end

function SL.validate_signed(signed, role, allowed_targets)
    SL.expect(type(signed) == "table", role .. ".signed must be an object")
    if role == "timestamp" then
        SL.exact_keys(signed, {
            "bundle_id", "channel", "expires_at", "issued_at", "min_loader_version", "role",
            "schema", "security_epoch", "snapshot", "version",
        }, "timestamp.signed")
        SL.validate_common(signed, role)
        SL.validate_meta_ref(signed.snapshot, "timestamp.snapshot")
        return nil
    end
    if role == "snapshot" then
        SL.exact_keys(signed, {
            "bundle_id", "channel", "expires_at", "issued_at", "min_loader_version", "role",
            "schema", "security_epoch", "targets", "version",
        }, "snapshot.signed")
        SL.validate_common(signed, role)
        SL.validate_meta_ref(signed.targets, "snapshot.targets")
        return nil
    end
    SL.expect(role == "targets", "unknown metadata role")
    return SL.validate_targets(signed, allowed_targets)
end

function SL.frame(name, value)
    SL.assert_ascii(name, "canonical field name", 1, 64)
    local text = type(value) == "number" and SL.integer_text(value) or value
    SL.assert_ascii(text, "canonical " .. name, 0, 1024)
    return #name .. ":" .. name .. #text .. ":" .. text
end

function SL.canonical_signed(signed, graph)
    local role = signed.role
    local parts = {
        "GSLOADER:" .. role .. ":v1\0",
        SL.frame("schema", signed.schema), SL.frame("role", role),
        SL.frame("bundle_id", signed.bundle_id), SL.frame("channel", signed.channel),
        SL.frame("version", signed.version), SL.frame("security_epoch", signed.security_epoch),
        SL.frame("issued_at", signed.issued_at), SL.frame("expires_at", signed.expires_at),
        SL.frame("min_loader_version", signed.min_loader_version),
    }
    if role == "timestamp" then
        parts[#parts + 1] = SL.frame("snapshot.version", signed.snapshot.version)
        parts[#parts + 1] = SL.frame("snapshot.length", signed.snapshot.length)
        parts[#parts + 1] = SL.frame("snapshot.sha256", signed.snapshot.sha256)
    elseif role == "snapshot" then
        parts[#parts + 1] = SL.frame("targets.version", signed.targets.version)
        parts[#parts + 1] = SL.frame("targets.length", signed.targets.length)
        parts[#parts + 1] = SL.frame("targets.sha256", signed.targets.sha256)
    else
        parts[#parts + 1] = SL.frame("rollback_floor", signed.rollback_floor)
        parts[#parts + 1] = SL.frame("entrypoint_count", #graph.entrypoints)
        for index = 1, #graph.entrypoints do
            parts[#parts + 1] = SL.frame("entrypoint", graph.entrypoints[index])
        end
        parts[#parts + 1] = SL.frame("module_count", #graph.module_ids)
        for index = 1, #graph.module_ids do
            local module = graph.modules[graph.module_ids[index]]
            for _, field in ipairs({ "id", "version", "owner", "repo", "commit", "path", "length", "sha256" }) do
                parts[#parts + 1] = SL.frame("module." .. field, module[field])
            end
            parts[#parts + 1] = SL.frame("module.dependency_count", #module.dependencies)
            for dependency_index = 1, #module.dependencies do
                parts[#parts + 1] = SL.frame("module.dependency", module.dependencies[dependency_index])
            end
            parts[#parts + 1] = SL.frame("module.capability_count", #module.capabilities)
            for capability_index = 1, #module.capabilities do
                parts[#parts + 1] = SL.frame("module.capability", module.capabilities[capability_index])
            end
        end
    end
    return table.concat(parts)
end

function SL.parse_envelope(raw, role)
    local envelope = SL.strict_json_parse(raw, role .. " envelope", SL.limits.metadata_bytes)
    SL.exact_keys(envelope, { "signatures", "signed" }, role .. " envelope")
    local graph = SL.validate_signed(envelope.signed, role, SL.config.allowed_targets)
    local signature_count = SL.array_length(envelope.signatures, role .. ".signatures", 1, 16)
    local signatures = {}
    local seen = {}
    for index = 1, signature_count do
        local signature = SL.exact_keys(envelope.signatures[index], { "key_id", "sig" }, role .. ".signature")
        local key_id = SL.valid_id(signature.key_id, role .. ".signature.key_id")
        SL.expect(not seen[key_id], role .. " has duplicate signature " .. key_id)
        seen[key_id] = true
        local decoded, decode_error = SL.base64_decode(signature.sig, 64)
        SL.expect(decoded ~= nil, role .. " signature encoding failed: " .. tostring(decode_error))
        signatures[index] = { key_id = key_id, sig = signature.sig }
    end
    return { signed = envelope.signed, signatures = signatures, graph = graph }
end

function SL.verify_envelope(raw, role)
    local envelope = SL.parse_envelope(raw, role)
    local policy = SL.config.roles[role]
    local permitted = {}
    for index = 1, #policy.key_ids do
        permitted[policy.key_ids[index]] = true
    end
    local canonical = SL.canonical_signed(envelope.signed, envelope.graph)
    local valid_signers = {}
    for index = 1, #envelope.signatures do
        local signature = envelope.signatures[index]
        SL.expect(permitted[signature.key_id], role .. " has unauthorized signer " .. signature.key_id)
        local key = SL.config.trusted_keys[signature.key_id]
        local verified, verify_error = SL.verify_p256(canonical, signature.sig, key.x, key.y)
        if verified == nil then
            SL.raise(role .. " verifier failure: " .. tostring(verify_error))
        end
        if verified then
            valid_signers[#valid_signers + 1] = signature.key_id
        end
    end
    SL.expect(#valid_signers >= policy.threshold,
        role .. " signature threshold not met (" .. #valid_signers .. "/" .. policy.threshold .. ")")
    table.sort(valid_signers)
    local digest, digest_error = SL.sha256_hex(raw)
    SL.expect(digest ~= nil, digest_error)
    return {
        raw = raw, signed = envelope.signed, graph = envelope.graph,
        valid_signers = valid_signers, digest = digest,
    }
end

function SL.config_foundation_valid()
    local ok, result = pcall(function()
        SL.exact_keys(CONFIG, {
            "allowed_targets", "bundle_id", "channel", "metadata_base_url", "roles", "trusted_keys",
        }, "public trust configuration")
        if CONFIG.metadata_base_url:find("OWNER", 1, true) or CONFIG.metadata_base_url:find("REPOSITORY", 1, true) then
            SL.raise("run publish.mjs configure-loader with production public trust")
        end
        SL.validate_metadata_base_url(CONFIG.metadata_base_url)
        SL.valid_id(CONFIG.bundle_id, "configuration bundle_id")
        SL.valid_id(CONFIG.channel, "configuration channel")
        local allowed_targets = SL.validate_allowed_targets(CONFIG.allowed_targets)
        SL.expect(type(CONFIG.trusted_keys) == "table", "trusted_keys must be an object")
        local trusted_keys = {}
        local key_count = 0
        for key_id, key in pairs(CONFIG.trusted_keys) do
            key_count = key_count + 1
            SL.valid_id(key_id, "trusted key id")
            SL.exact_keys(key, { "x", "y" }, "trusted key " .. key_id)
            local x, x_error = SL.hex_decode(key.x, 32)
            local y, y_error = SL.hex_decode(key.y, 32)
            SL.expect(x ~= nil and y ~= nil, "trusted key " .. key_id .. " has malformed coordinates: "
                .. tostring(x_error or y_error))
            SL.expect(key.x ~= string.rep("0", 64) and key.y ~= string.rep("0", 64),
                "trusted key " .. key_id .. " is still a placeholder")
            trusted_keys[key_id] = { x = key.x, y = key.y }
        end
        SL.expect(key_count >= 1 and key_count <= 16, "trusted key count is outside 1-16")
        SL.exact_keys(CONFIG.roles, { "snapshot", "targets", "timestamp" }, "roles")
        local roles = {}
        for _, role in ipairs({ "timestamp", "snapshot", "targets" }) do
            local policy = SL.exact_keys(CONFIG.roles[role], { "key_ids", "threshold" }, role .. " role policy")
            local key_ids = SL.sorted_unique_strings(policy.key_ids, role .. ".key_ids", SL.valid_id, 16)
            SL.expect(#key_ids >= 1, role .. " role must contain a key")
            SL.assert_integer(policy.threshold, role .. ".threshold", 1, #key_ids)
            for index = 1, #key_ids do
                SL.expect(trusted_keys[key_ids[index]] ~= nil, role .. " references unknown key " .. key_ids[index])
            end
            roles[role] = { threshold = policy.threshold, key_ids = key_ids }
        end
        SL.config = {
            metadata_base_url = CONFIG.metadata_base_url,
            bundle_id = CONFIG.bundle_id,
            channel = CONFIG.channel,
            allowed_targets = allowed_targets,
            trusted_keys = trusted_keys,
            roles = roles,
        }
        return true
    end)
    if not ok then
        return nil, result
    end
    return result
end

