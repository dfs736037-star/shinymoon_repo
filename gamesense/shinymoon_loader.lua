function SL.assert_ascii(value, label, minimum, maximum)
    SL.expect(type(value) == "string", label .. " must be a string")
    SL.expect(#value >= (minimum or 1) and #value <= (maximum or 256), label .. " length is outside bounds")
    for index = 1, #value do
        local byte = value:byte(index)
        SL.expect(byte >= 0x20 and byte <= 0x7e, label .. " must be printable ASCII")
    end
    return value
end

function SL.assert_integer(value, label, minimum, maximum)
    local upper = maximum or 9007199254740991
    SL.expect(type(value) == "number" and value == math.floor(value), label .. " must be an integer")
    SL.expect(value >= (minimum or 0) and value <= upper, label .. " is outside integer bounds")
    return value
end

